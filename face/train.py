import numpy as np
import matplotlib.pyplot as plt

def plot_training_history(history):
    """Plot training history"""
    plt.figure(figsize=(12, 4))

    # Plot accuracy
    plt.subplot(1, 2, 1)
    plt.plot(history.history['accuracy'], label='Training Accuracy')
    plt.plot(history.history['val_accuracy'], label='Validation Accuracy')
    plt.title('Model Accuracy')
    plt.xlabel('Epoch')
    plt.ylabel('Accuracy')
    plt.legend()

    # Plot loss
    plt.subplot(1, 2, 2)
    plt.plot(history.history['loss'], label='Training Loss')
    plt.plot(history.history['val_loss'], label='Validation Loss')
    plt.title('Model Loss')
    plt.xlabel('Epoch')
    plt.ylabel('Loss')
    plt.legend()

    plt.tight_layout()
    plt.savefig('training_history.png')
    plt.show()

def train_emotion_model():
    """Train the emotion recognition model"""
    print("ğŸš€ Starting Emotion Recognition Model Training")
    print("=" * 60)

    # Load dataset
    loader = DataLoader()
    # Changed load_dataset to load_kaggle_dataset based on debugging output
    X_train, X_test, y_train, y_test = loader.load_kaggle_dataset()

    if X_train is None:
        print("âŒ Failed to load dataset. Cannot train model.")
        return None

    print(f"\nğŸ“Š Training set: {X_train.shape[0]} samples")
    print(f"ğŸ“Š Test set: {X_test.shape[0]} samples")
    print(f"ğŸ“ Input shape: {X_train.shape[1:]}")

    # Build and train model
    print("\nğŸ§  Building CNN Model...")
    model = EmotionCNN()
    model.compile_model(learning_rate=0.001)

    # Display model summary
    print("\nğŸ“‹ Model Architecture:")
    model.model.summary()

    print("\nğŸ¯ Starting Training...")
    print("This will take a while. Please wait...")

    # Train with fewer epochs for faster results
    history = model.train(X_train, y_train, X_test, y_test, epochs=30, batch_size=32)

    # Evaluate the model
    print("\nğŸ“ˆ Evaluating model...")
    train_loss, train_accuracy = model.model.evaluate(X_train, y_train, verbose=0)
    test_loss, test_accuracy = model.model.evaluate(X_test, y_test, verbose=0)

    print(f"âœ… Final Training Accuracy: {train_accuracy:.4f}")
    print(f"âœ… Final Test Accuracy: {test_accuracy:.4f}")

    # Save the model
    model_path = 'emotion_model.h5'
    model.save_model(model_path)

    # Plot training history
    plot_training_history(history)

    print(f"\nğŸ‰ Training Completed!")
    print(f"ğŸ’¾ Model saved as: {model_path}")

    return model_path

